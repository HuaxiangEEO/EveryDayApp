export function getRandomWord(category?: (typeof WORD_CATEGORIES)[number]): string {
  const cat = category ?? WORD_CATEGORIES[Math.floor(Math.random() * WORD_CATEGORIES.length)]
  const list = WORDS[cat]
  return list[Math.floor(Math.random() * list.length)].trim()
}

export function getAllWords(): string[] {
  return Object.values(WORDS).flat().map((w) => w.trim())
}
